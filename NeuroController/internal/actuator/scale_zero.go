// 📄 scale_zero.go
//
// ✨ 功能说明：
//     本模块用于将指定 Deployment 的副本数缩容为 0，用于临时冻结异常服务，
//     以防止异常 Pod 无限重启占用资源或触发误报警。该操作可被 watcher 或策略模块触发。
//
// 🛠️ 提供功能：
//     - ScaleDeploymentToZero(namespace, deployment string): 缩容目标 Deployment 至 0 副本
//
// 📦 依赖：
//     - k8s.io/client-go/kubernetes
//     - k8s.io/apimachinery/pkg/apis/meta/v1
//
// 📍 使用场景：
//     - 异常 Pod 检测到多次失败后触发缩容
//     - 策略引擎决定主动隔离某服务
//
// 🛡️ 注意事项：
//     - 不适用于 StatefulSet 等持久化副本控制器
//     - 实际生产中建议配合邮件/审计系统使用
//
// ✍️ 作者：武夏锋（@ZGMF-X10A）
// 📅 创建时间：2025-06
// =======================================================================================

