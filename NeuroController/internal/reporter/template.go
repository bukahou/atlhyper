// ✨ 功能说明：
//     本模块用于渲染报警邮件的 HTML 模板，提升告警信息的可读性与结构化程度。
//     使用标准库 text/template 加载 `mail_template.tmpl`，
//     支持自定义字段（时间、原因、日志内容、建议等）。
//
// 🛠️ 提供功能：
//     - RenderEmailTemplate(data EmailTemplateData) (string, error): 返回渲染后的 HTML 字符串
//
// 📦 依赖：
//     - text/template（标准库）
//     - config/mail_template.tmpl（模板文件）
//
// 📍 使用场景：
//     - 被 email.go 调用，生成结构化邮件内容
//     - 可拓展为多语言模板、样式主题切换
//
// 🧩 模板字段建议：
//     - Time：告警时间
//     - Reason：异常类型或触发策略
//     - Logs：截断日志内容
//     - Suggestion：AI 或规则提供的应对建议
