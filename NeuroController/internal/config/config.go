// ✨ 功能说明：
//     本模块负责加载与提供控制器的全局配置项，
//     包括模块开关、告警邮箱配置、策略阈值、Webhook 映射等。
//     默认从 config.yaml 加载内容，并提供统一访问接口（如 GetEmailConfig）。
//
// 🛠️ 提供功能：
//     - LoadConfig(): 初始化加载 config.yaml 内容
//     - IsModuleEnabled(name string): 判断某模块是否启用
//     - GetEmailConfig(), GetThresholds(): 获取模块配置项
//
// 📦 依赖：
//     - gopkg.in/yaml.v2
//     - config.yaml（同目录）
//
// 📍 使用场景：
//     - 启动阶段加载配置项
//     - 各模块获取共享参数或模块启用状态
//
// 🧩 可扩展点：
//     - 热更新配置（使用 fsnotify）
//     - 多环境配置分离（dev/stage/prod）
//
